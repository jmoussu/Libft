#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dest;

	if (!(char*)malloc(sizeof(char) * (ft_strlen(src) + 1)))
		return (NULL);
	dest = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	i = 0;
	while (src[i] != 0)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
